// ========== 오타/한글숫자 교정 사전 ==========
export const TYPO_MAP: Record<string, string> = {
  // 한글 숫자
  '일억':'1억','이억':'2억','삼억':'3억','사억':'4억','오억':'5억',
  '육억':'6억','칠억':'7억','팔억':'8억','구억':'9억','십억':'10억',
  '일천':'1천','이천':'2천','삼천':'3천','사천':'4천','오천':'5천',
  '육천':'6천','칠천':'7천','팔천':'8천','구천':'9천',
  // 거래유형
  'ㅈㅅ':'전세','젼세':'전세','ㅁㅁ':'매매','ㅇㅅ':'월세','웜세':'월세','웜ㄴ세':'월세',
  // 카테고리
  '아빠트':'아파트','옵텔':'오피스텔','오피스탤':'오피스텔','상과':'상가',
  // 가격 표현
  '안넘게':'이하','안쪽':'이하','이해':'이하','안됨':'이하','넘지않게':'이하',
};

// 텍스트에 오타 교정 적용
export function fixTypos(text: string): string {
  let result = text;
  for (const [typo, fix] of Object.entries(TYPO_MAP)) {
    if (result.includes(typo)) result = result.replace(new RegExp(typo, 'g'), fix);
  }
  return result;
}
